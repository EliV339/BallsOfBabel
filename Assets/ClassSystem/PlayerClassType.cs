/// <summary>
/// Defines the three player classes available in Balls of Babel.
/// </summary>
public enum PlayerClassType
{
    None   = 0,
    Light  = 1,   // Fast, crit-based damage, grapple + rescue dash
    Healer = 2,   // Support, heal beam, AOE heal, uber meter
    Tank   = 3    // Slow, shield HP, heavy slam, bubble shield
}
